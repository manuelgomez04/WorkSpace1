package ejercicio03;

public class Operaciones {

	
	public boolean comprobarParOImpar (int num, int dos) {
		
		if (num % dos == 0) {
			
			return true;
			
		} else {
			
			return false;
		}
		
	}
	
	public boolean comprobarPositivoONegativo (int num, int cero) {
		
		if (num >= cero) {
			
			return true;
			
		}else {
			
			return false;
		}
	}
	
	// public void mostrarParOImpar (boolean true, boolean false)
	
	
}

